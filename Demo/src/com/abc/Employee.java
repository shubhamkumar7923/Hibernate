package com.abc;

public class Employee {

	private int eid;
	private String nme;
	private int sal;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getNme() {
		return nme;
	}
	public void setNme(String name) {
		this.nme = name;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}
	
	
}
